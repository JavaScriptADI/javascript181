let a = 7;
a += 1; // a = a + 1;
a++; // a = a + 1;

console.log(a);
a -= 1; // a = a - 1;
a--; // a = a - 1;
console.log(a);

a *= 2; // a = a * 2
console.log(a);
a /= 7; // a = a / 7
console.log(a);

// Arithmetic operators
a = 3;
console.log(`${a} ** 2 = ${a ** 2}`);
console.log(`${a} ** 3 = ${a ** 3}`);
console.log(`${a} ** 4 = ${a ** 4}`);
console.log(`${a} ** 5 = ${a ** 5}`);
a **= 3; // a = a ** 3
console.log(`${a} % 2 = ${a % 2}`);
a %= 3; // a = a % 3
console.log(a);

